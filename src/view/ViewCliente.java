    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int linha = this.jTableCliente.getSelectedRow();
        int codigoCliente = (int) jTableCliente.getValueAt(linha, 0);
        if(controllerCliente.excluirClienteController(codigoCliente)){
            JOptionPane.showMessageDialog(this, "CLIENTE EXCLUÍDO COM SUCESSO!", "MANUTENÇÃO DE CLIENTE", JOptionPane.INFORMATION_MESSAGE);
            preencherTabelaCliente();
            habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "ERRO AO EXCLUIR CLIENTE!", "MANUTENÇÃO DE CLIENTE", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        int linha = this.jTableCliente.getSelectedRow();
        try{
            int codigoCliente = (int) jTableCliente.getValueAt(linha, 0);
            modelCliente = controllerCliente.getClienteController(codigoCliente);
            this.jTextFieldCodigo.setText(String.valueOf(modelCliente.getIdCliente()));
            this.jTextFieldNome.setText(modelCliente.getCliNome());
            this.jTextFieldEndereco.setText(modelCliente.getCliEndereco());
            this.jTextFieldBairro.setText(modelCliente.getCliBairro());
            this.jTextFieldCidade.setText(modelCliente.getCliCidade());
            this.jComboBoxUf.setSelectedItem(modelCliente.getCliUf());
            this.jTextFieldCep.setText(modelCliente.getCliCep());
            this.jTextFieldTelefone.setText(modelCliente.getCliTelefone());
            habilitarDesabilitarCampos(true);
        }catch(Exception e){
            habilitarDesabilitarCampos(false);
            JOptionPane.showMessageDialog(this, "CÓDIGO INVÁLIDO OU NENHUM REGISTRO SELECIONADO!", "MANUTENÇÃO DE CLIENTE", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonAlterarActionPerformed

