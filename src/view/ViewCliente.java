    /**
     * Preeche a tabela com uma lista de clientes
     */
    private void preencherTabelaCliente(){
        listaModelClientes = controllerCliente.getListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) jTableCliente.getModel();
        modelo.setNumRows(0);
        int cont = listaModelClientes.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
            listaModelClientes.get(i).getIdCliente(),
            listaModelClientes.get(i).getCliNome(),
            listaModelClientes.get(i).getCliCidade(),
            listaModelClientes.get(i).getCliTelefone()
            });   
        }
    }
    
    /**
     * Salva um cliente no banco
     */
    private void salvarCliente(){
        modelCliente.setCliNome(this.jTextFieldNome.getText());
        modelCliente.setCliEndereco(this.jTextFieldEndereco.getText());
        modelCliente.setCliBairro(this.jTextFieldBairro.getText());
        modelCliente.setCliCidade(this.jTextFieldCidade.getText());
        modelCliente.setCliUf(this.jComboBoxUf.getSelectedItem().toString());
        modelCliente.setCliCep(this.jTextFieldCep.getText());
        modelCliente.setCliTelefone(this.jTextFieldTelefone.getText());
        
        if(controllerCliente.salvarClienteController(modelCliente) > 0){
            JOptionPane.showMessageDialog(this, "CLIENTE CADASTRADO COM SUCESSO!", "MANUTENÇÃO DE CLIENTE", JOptionPane.INFORMATION_MESSAGE);
            //Carregar clientes na tabela
            preencherTabelaCliente();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "ERRO AO CADASTRAR CLIENTE!", "MANUTENÇÃO DE CLIENTE", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    /**
     * Altera um cliente no banco
     */
    public void alteraCliente(){
        modelCliente.setCliNome(this.jTextFieldNome.getText());
        modelCliente.setCliEndereco(this.jTextFieldEndereco.getText());
        modelCliente.setCliBairro(this.jTextFieldBairro.getText());
        modelCliente.setCliCidade(this.jTextFieldCidade.getText());
        modelCliente.setCliUf(this.jComboBoxUf.getSelectedItem().toString());
        modelCliente.setCliCep(this.jTextFieldCep.getText());
        modelCliente.setCliTelefone(this.jTextFieldTelefone.getText());
        
        if(controllerCliente.atualizarClienteController(modelCliente)){
            JOptionPane.showMessageDialog(this, "CLIENTE ALTERADO COM SUCESSO!", "MANUTENÇÃO DE CLIENTE", JOptionPane.INFORMATION_MESSAGE);
            //Carregar clientes na tabela
            preencherTabelaCliente();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "ERRO AO ALTERAR CLIENTE!", "MANUTENÇÃO DE CLIENTE", JOptionPane.ERROR_MESSAGE);
        }
    }
    
